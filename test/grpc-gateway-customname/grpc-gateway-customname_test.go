package customname

import (
	context "context"
	"testing"

	"github.com/grpc-ecosystem/grpc-gateway/runtime"
	grpc "google.golang.org/grpc"
)

/*
Compilation fails if a proto file uses gogoproto.customname
in combination with protoc-gen-grpc-gateway

this is due to the fact that protoc-gen-grpc-gateway does not know
the field name is altered by gogoproto.

the file generated by protoc-gen-grpc-gateway will try to access CamelCase version
of id (Id), not to the new custom fieldname (Example ID).

issue:

rpc GetBar (GetBarRequest) returns (GetBarResponse) {
	option (google.api.http) = {
		get: "/api/v1/bar/{id}"
	};
}

message GetBarRequest {
    string id = 1 [(gogoproto.customname) = "ID"];
}

the new protoc plugin protoc-gen-gogogatway fixes this issue
by wrapping protoc-gen-grpc-gateway plugin and fixing the generated content

*/
func TestShouldCompile(t *testing.T) {
	// this should compile if the custom name fix has been applied
	ctx := context.Background()
	mux := runtime.NewServeMux()
	opts := []grpc.DialOption{grpc.WithInsecure()}
	err := RegisterAHandlerFromEndpoint(ctx, mux, "", opts)
	if err != nil {
		t.Fatal("Unable to register handler")
	}
}
